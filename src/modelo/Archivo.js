class Archivo {

  nombre
  ruta
  fecha_creacion
  fecha_actualizacion

  constructor(nombre, ruta, fecha_creacion, fecha_actualizacion){
    this.nombre = nombre
    this.ruta = ruta
    this.fecha_creacion = fecha_creacion
    this.fecha_actualizacion = fecha_actualizacion
  }

}

module.exports = Archivo